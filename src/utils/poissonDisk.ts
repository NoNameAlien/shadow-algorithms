export function getPoissonDisk(sampleCount: number): Float32Array {
    if (sampleCount <= 8) return POISSON_DISK_8;
    if (sampleCount <= 16) return POISSON_DISK_16;
    if (sampleCount <= 32) return POISSON_DISK_32;
    return POISSON_DISK_64;
}

export function getPoissonDiskCount(sampleCount: number): number {
    if (sampleCount <= 8) return 8;
    if (sampleCount <= 16) return 16;
    if (sampleCount <= 32) return 32;
    return 64;
}
